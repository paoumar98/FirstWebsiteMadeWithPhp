			<h4>1.LES OBJECTIFS PEDAGOGIQUES DE FORMATION</h4>
L’objectif d’une action de formation professionnelle au profit de salariés d’une entreprise définit le but précis
qu’elle se propose d’atteindre, et vise une évolution des savoirs et savoirs-faire des salariés à partir de leurs
compétences (Circ. n°37 du 14 mars 1986, Ministère du Travail).
Ils servent à construire, conduire et évaluer les actions de formation. Les objectifs sont rédigés en termes “être
capable de, pouvoir définir ”.
<h4>2.LES PREREQUIS OU LE PUBLIC CONCERNE</h4>
Le pré-requis consiste en la maîtrise des savoirs et savoirs-faire nécessaires pour suivre valablement un module
de formation ou une formation complète.
<h4>3.LA PROGRESSION PEDAGOGIQUE (OU CONTENU)</h4>
Le programme d’une action de formation retrace les différentes étapes intermédiaires à parcourir par le stagiaire
en vue d’atteindre l’objectif final visé. Il peut s’agir d’acquisition de connaissances théoriques, de gestes et de
techniques professionnels…
<h4>4.LES MOYENS PEDAGOGIQUES</h4>
Il s’agit des moyens matériels de la formation : locaux (salles, ateliers), supports de formation tels que manuels,
matériels informatiques, audiovisuel, machines…
<h4>5.LES MOYENS D’EVALUATION MIS EN ŒUVRE ET SUIVI</h4>
Contrôle des connaissances et reconnaissances des acquis, validation par un diplôme, mais aussi évaluation de
l’action de formation en fonction des objectifs visés par les stagiaires, par les employeurs et par l’organisme luimême.

La réalité de l’action : la liste d’émargement et l’attestation de présence sont des obligations minimales
<h4>6.L’ENCADREMENT D’UNE ACTION DE FORMATION</h4>
Ce sont les formateurs qui ont des compétences d’ordre technique, professionnel ou théorique mais aussi
pédagogique, reconnues par des diplômes ou issues d’une expérience professionnelle…
<h4>7.LA DUREE DE FORMATION</h4>
La pratique du contrôle par l’administration conduit souvent au rejet du caractère imputable pour une action
d’une durée inférieure à la journée. En revanche, un programme de formation peut comporter plusieurs modules
de quelques heures.
